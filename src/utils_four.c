#include "../include/minishell.h"

void	ft_pwd(void)
{
	char	*src;

	src = ft_get_env("PWD");
	if (!src)
		printf("NO PWD\n");
	else
		printf("%s\n", src);
}

char	*search_op(char *src)
{
	if (ft_strnstr(src, "<", ft_strlen(src)))
		return (src);
	if (ft_strnstr(src, ">", ft_strlen(src)))
		return (src);
	if (ft_strnstr(src, "<<", ft_strlen(src)))
		return (src);
	if (ft_strnstr(src, ">>", ft_strlen(src)))
		return (src);
	if (ft_strnstr(src, "|", ft_strlen(src)))
		return (src);
	return (NULL);
}